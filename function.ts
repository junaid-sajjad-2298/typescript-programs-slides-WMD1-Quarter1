// Concrete function example
function multiply(a: number, b: number): number {
    return a * b;
  }
  console.log(multiply(3, 4)); // Output: 12
  
  // Arrow function example
  const divide = (a: number, b: number): number => {
    return a / b;
  };
  console.log(divide(10, 5)); // Output: 2

  